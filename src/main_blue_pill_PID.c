// PID controller function
float pid_controller(float current_value) {
    error = setpoint -current_value; // 2 voltios
	
    integral += error;
    derivative = error - prev_error;
    
    // Calculate control output
    float output = KI * integral + KD * derivative+ KP * error; // + 
    
    // Update previous error
    prev_error = error;
    //output=current_value;
    return output;
}

